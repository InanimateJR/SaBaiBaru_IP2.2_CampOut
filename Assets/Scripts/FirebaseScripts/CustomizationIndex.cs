public class CustomizationIndex
{
    public int foldedTentMaterial;
    public int tentMaterial;
    public int bagMaterial;

public CustomizationIndex()
{
}

public CustomizationIndex(int foldedTentMaterial, int tentMaterial, int bagMaterial)
{
    this.foldedTentMaterial = foldedTentMaterial;
    this.tentMaterial = tentMaterial;
    this.bagMaterial = bagMaterial;
}
}
